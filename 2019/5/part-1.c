#define PARAM_1(in)  (((in->op) / 100  ) % 10)
#define PARAM_2(in)  (((in->op) / 1000 ) % 10)
#define PARAM_3(in)  (((in->op) / 10000) % 10)
#define GET_INSTR(in) ((in->op) % 100)

